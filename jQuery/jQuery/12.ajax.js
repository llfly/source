jQuery.fn.extend({
	serialize: function() {
		return jQuery.param( this.serializeArray() );
	},
	serializeArray: function() {
		return this.map(function(){//this为form或单独元素input
			// Can add propHook for "elements" to filter or add form elements
			var elements = jQuery.prop( this, "elements" );//只有form有elements这个属性,jquery中通过elements属性获取对应值
			return elements ? jQuery.makeArray( elements ) : this;//得到筛选后的jQuery对象
		})
		.filter(function(){
			var type = this.type;
			// Use .is(":disabled") so that fieldset[disabled] works
			return this.name && !jQuery( this ).is( ":disabled" ) &&//检测每一个input是否有name并且不是被禁用的
				rsubmittable.test( this.nodeName ) && !rsubmitterTypes.test( type ) &&//排除元素rsubmitterTypes中不能有name，rsubmittable中含有name
				( this.checked || !manipulation_rcheckableType.test( type ) );//针对单选框多选框是否被选中
		})
		.map(function( i, elem ){
			var val = jQuery( this ).val();

			return val == null ?
				null :
				jQuery.isArray( val ) ?
					jQuery.map( val, function( val ){
						return { name: elem.name, value: val.replace( rCRLF, "\r\n" ) };
					}) :
					{ name: elem.name, value: val.replace( rCRLF, "\r\n" ) };
		}).get();
	}
});

//Serialize an array of form elements or a set of
//key/values into a query string
//将json/array序列化成字符串
jQuery.param = function( a, traditional ) {//a数据，traditional是否是传统操作方式
	var prefix,
		s = [],
		add = function( key, value ) {
			// If value is a function, invoke it and return its value
			value = jQuery.isFunction( value ) ? value() : ( value == null ? "" : value );//判断a是否是function
			s[ s.length ] = encodeURIComponent( key ) + "=" + encodeURIComponent( value );
		};

	// Set traditional to true for jQuery <= 1.3.2 behavior.
	if ( traditional === undefined ) {
		traditional = jQuery.ajaxSettings && jQuery.ajaxSettings.traditional;
	}

	// If an array was passed in, assume that it is an array of form elements.
	// 如果是数组的话，或者jQuery对象,通过name和value来添加
	if ( jQuery.isArray( a ) || ( a.jquery && !jQuery.isPlainObject( a ) ) ) {
		// Serialize the form elements
		jQuery.each( a, function() {
			add( this.name, this.value );
		});

	} else {
		// If traditional, encode the "old" way (the way 1.3.2 or older
		// did it), otherwise encode params recursively.
		for ( prefix in a ) {
			buildParams( prefix, a[ prefix ], traditional, add );//分支处理(key,value,boolean,callback)
		}
	}

	// Return the resulting serialization
	return s.join( "&" ).replace( r20, "+" );//%20 空格替换成+
};

function buildParams( prefix, obj, traditional, add ) {
	var name;

	if ( jQuery.isArray( obj ) ) {//判断value是否是数组
		// Serialize array item.
		jQuery.each( obj, function( i, v ) {
			if ( traditional || rbracket.test( prefix ) ) {//是否是传统方式，如果是直接add  || rbracket匹配key左右中括号
				// Treat each array item as a scalar.
				add( prefix, v );

			} else {//正常情况 通过递归 将key值重新拼接
				// Item is non-scalar (array or object), encode its numeric index.
				buildParams( prefix + "[" + ( typeof v === "object" ? i : "" ) + "]", v, traditional, add );
			}
		});

	} else if ( !traditional && jQuery.type( obj ) === "object" ) {//处理对象情况下,将key值取出来，拼接到之前的key上，继续递归处理
		// Serialize object item.
		for ( name in obj ) {
			buildParams( prefix + "[" + name + "]", obj[ name ], traditional, add );
		}

	} else {//默认处理方式
		// Serialize scalar item.
		add( prefix, obj );
	}
}
